    orange_dk = cols_flatui_real$pumpkin,
    red       = cols_flatui_real$alizarin,
    red_dk    = cols_flatui_real$pomegranate,
    grey_1    = cols_flatui_real$clouds,
    grey_2    = cols_flatui_real$silver,
    grey_3    = cols_flatui_real$concrete,
    grey_4    = cols_flatui_real$asbestos,
    grey_5    = cols_flatui_real$wetasphalt,
